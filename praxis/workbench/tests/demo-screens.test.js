'use strict';
/* The seeded First Review runs in the two worked examples must be the runs the app
   itself would have produced. They were generated by calling buildScreenItems on the
   demo's own context, and nothing keeps them that way except this file.

   What it guards, in order of how badly it would fail the user:
     1. DRIFT. buildScreenItems reads the demo's matrix, gate snapshot, design, sample
        plan, report outline and decision windows. Any change to those artifacts, or to
        the generator itself, changes the checklist a real reviewer would get. The seeded
        runs would then be showing a demo user a checklist the tool no longer builds:
        a worked example that lies. So every seeded run's item ids are regenerated from
        the live code and compared EXACTLY, in order.
     2. UNANSWERED ITEMS. A worked example must not trip the "N of M items were not
        answered" disclosure. Every non-auto item carries an answer.
     3. FAKED TIMING. timing:window is COMPUTED from the attached deliverable's
        submitted_at (or, on an unattached self-screen, from the date the screen was run)
        against the earliest decision window. It can never be hand-set, and the stored
        answer must equal what the engine computes.
     4. FAKED RECOMMENDATION. verdict_recommended must be the verdict recommendVerdict
        actually produces from the stored answers.
     5. THE STORY. The GF commissioner returns the report. The ZD commissioner records
        'reserved' against a recommendation of 'return': the tool recommends, the human
        decides, and that override is the point of the design.
     6. PRIVACY. No seeded run carries a prescan or any machine_evidence. The pre-scan is
        demonstrated live from the importable draft report; it is never shipped at rest.

   REGENERATION REFERENCE DATE. An unattached run has no submitted_at, so buildScreenItems
   falls back to "today" for the timing computation. Regenerating with the real today would
   make this test's expectations move with the calendar. It regenerates with the run's own
   started_at instead, which is both deterministic and what actually happened: the screen
   was run on that date. When a deliverable IS attached its submitted_at wins and the
   reference date is ignored, so the same call is correct for every run. */
var H = require('./helpers');
var W = H.loadWorkbench(['js/demo-data-gf.js', 'js/demo-data-zd.js']);
var C = W.PraxisScreenCore;

var DEMOS = [
  { name: 'GF', ctx: W.PRAXIS_DEMO_GF, runs: 2 },
  { name: 'ZD', ctx: W.PRAXIS_DEMO_ZD, runs: 2 }
];

function deliverableOf(ctx, id) {
  if (!id) return null;
  return ((ctx.planning || {}).deliverables || []).filter(function(d) { return d.id === id; })[0] || null;
}

DEMOS.forEach(function(demo) {
  var ctx = demo.ctx;
  var tag = demo.name + ': ';
  H.assert(!!ctx, tag + 'demo fixture loads');
  var runs = ctx.report_screens;
  H.assert(Array.isArray(runs), tag + 'report_screens is a top-level array');
  H.eq(runs.length, demo.runs, tag + 'seeded run count');

  runs.forEach(function(run) {
    var rt = tag + run.id + ': ';
    var del = deliverableOf(ctx, run.deliverable_id);
    if (run.deliverable_id) H.assert(!!del, rt + 'the attached deliverable exists in the demo');

    // (1) drift guard: the seeded checklist is the checklist the app builds today.
    var built = C.buildScreenItems(ctx, {
      deliverable: del,
      todayIso: String(run.started_at).slice(0, 10)
    });
    var seededIds = run.items.map(function(it) { return it.id; });
    var builtIds = built.map(function(it) { return it.id; });
    H.eq(seededIds.join('|'), builtIds.join('|'),
      rt + 'seeded item ids are exactly what buildScreenItems generates, in order');

    // (2) a worked example never shows "N of M items were not answered".
    var unanswered = run.items.filter(function(it) { return !it.auto && !it.answer; });
    H.eq(unanswered.length, 0, rt + 'every non-auto item carries an answer');

    // (3) the timing verdict is computed, never seeded by hand.
    var seededTiming = run.items.filter(function(it) { return it.id === 'timing:window'; })[0];
    var builtTiming = built.filter(function(it) { return it.id === 'timing:window'; })[0];
    H.assert(!!seededTiming && !!builtTiming, rt + 'the run carries the computed timing item');
    H.assert(seededTiming.auto === true, rt + 'the timing item is marked auto');
    H.eq(seededTiming.answer, builtTiming.answer, rt + 'stored timing answer equals the computed one');

    // (4) the stored recommendation is the one the engine produces.
    H.eq(C.recommendVerdict(run.items).verdict, run.verdict_recommended,
      rt + 'verdict_recommended is what recommendVerdict returns');
    H.assert(!!run.verdict, rt + 'a verdict is recorded');
    H.assert(!!run.completed_at, rt + 'the run is completed');
    H.assert(!!(run.reviewer || '').trim(), rt + 'the run names its reviewer');

    // (6) nothing from a pre-scan is shipped at rest.
    H.eq(run.prescan, null, rt + 'no prescan is seeded');
    var evidence = run.items.filter(function(it) { return it.machine_evidence; });
    H.eq(evidence.length, 0, rt + 'no item carries machine_evidence');
    var signals = run.items.filter(function(it) { return it.machine_signal; });
    H.eq(signals.length, 0, rt + 'no item carries a machine signal');
  });

  // the first-review act is on the accountability log.
  var log = (ctx.commissioner.audit_log || []).filter(function(e) { return e && e.action === 'first_review'; });
  H.eq(log.length, 1, tag + 'the commissioner first review is recorded in the audit log');
  H.assert(!!log[0].id && !!log[0].at && !!log[0].actor && !!log[0].detail,
    tag + 'the audit entry has the shape { id, at, actor, action, detail }');
});

// ---- (5) the story each demo tells ------------------------------------------
function runOf(ctx, role) {
  return ctx.report_screens.filter(function(r) { return r.role === role; })[0];
}
var gfComm = runOf(W.PRAXIS_DEMO_GF, 'commissioner');
H.eq(gfComm.verdict, 'return', 'GF: the commissioner returned the report');
H.eq(gfComm.verdict_recommended, 'return', 'GF: and the tool recommended exactly that');
H.assert(C.computeRedFlags(gfComm.items).length > 0, 'GF: the return rests on red flags');
H.assert(gfComm.items.some(function(it) { return it.id === 'uneg:limitations' && it.answer === 'no'; }),
  'GF: the undisclosed limitations the team self-screen missed are the commissioner run red flag');

var gfTeam = runOf(W.PRAXIS_DEMO_GF, 'team');
H.eq(gfTeam.deliverable_id, null, 'GF: the team self-screen ran before submission, on no deliverable');
H.eq(C.computeRedFlags(gfTeam.items).length, 0, 'GF: the team found no red flag in its own draft');
H.assert(gfTeam.items.some(function(it) { return it.id === 'uneg:limitations' && it.answer === 'yes'; }),
  'GF: the team passed itself on limitations, which is the miss a self-screen cannot catch');

var zdComm = runOf(W.PRAXIS_DEMO_ZD, 'commissioner');
H.eq(zdComm.verdict_recommended, 'return', 'ZD: the tool recommended a return');
H.eq(zdComm.verdict, 'reserved', 'ZD: and the human recorded accept-with-reservations instead');
H.assert(zdComm.items.some(function(it) { return it.id === 'timing:window' && it.answer === 'no'; }),
  'ZD: the headline red flag is the computed timing, which needed no reading at all');
H.assert(!!(zdComm.note || '').trim(), 'ZD: the override carries a written justification');

H.summary('demo-screens');
